/**
 * Industry_data_table helper.
 */
class Industry_data_tableHelper
{
	/**
	 * Configure the Linkbar.
	 */
	public static function addSubmenu($vName = '')
	{
		JHtmlSidebar::addEntry(
			JText::_('COM_INDUSTRY_DATA_TABLE_TITLE_AIRCRAFTDEALS'),
			'index.php?option=com_industry_data_table&view=aircraftdeals',
			$vName == 'aircraftdeals'
		);
		JHtmlSidebar::addEntry(
			JText::_('COM_INDUSTRY_DATA_TABLE_TITLE_EXPORTIMPORTDATAS'),
			'index.php?option=com_industry_data_table&view=exportimportdatas',
			$vName == 'exportimportdatas'
		);

	}

	/**
	 * Gets a list of the actions that can be performed.
	 *
	 * @return	JObject
	 * @since	1.6
	 */
	public static function getActions()
	{
		$user	= JFactory::getUser();
		$result	= new JObject;

		$assetName = 'com_industry_data_table';

		$actions = array(
			'core.admin', 'core.manage', 'core.create', 'core.edit', 'core.edit.own', 'core.edit.state', 'core.delete'
		);

		foreach ($actions as $action) {
			$result->set($action, $user->authorise($action, $assetName));
		}

		return $result;
	}
}
